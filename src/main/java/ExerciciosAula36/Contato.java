package ExerciciosAula36;

public class Contato {
	String nome;
	String email;
	String numero;
	
	public Contato(String nome, String email, String numero) {
		this.nome = nome;
		this.email = email;
		this.numero = numero;
		
	}

	public String getNome() {
		return nome;
	}


	public String getEmail() {
		return email;
	}

	public String getNumero() {
		return numero;
	}



}
