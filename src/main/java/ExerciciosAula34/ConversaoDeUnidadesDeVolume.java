package ExerciciosAula34;

public class ConversaoDeUnidadesDeVolume {
	
	double litrosParaCentimetrosCubicos(double litro) {
        return litro * 1000;
    }

    double metroCubicoParaLitros(int metrosCubico) {
        return metrosCubico * 1000;
    }

    double metroCubicoParaPesCubicos(double metrosCubicos) {
        return metrosCubicos * 35.32;
    }

    double galaoAmericoParaPolegadasCubicas(double galaoAmerico) {
        return galaoAmerico * 231;
        
    }
    double galaoAmericoParaLitros(double galaoAmericoLitro) {
        return galaoAmericoLitro * 3.785;
    
    }
}


