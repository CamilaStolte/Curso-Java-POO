package ExerciciosAula17;

public class Exercicio06 {

	public static void main(String[] args) {
        System.out.println("Imprimir um embaixo do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        System.out.println("Imprimir um do lado do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }
    }
}

